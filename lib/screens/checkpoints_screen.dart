    return Container(
      width: 32,
      height: 32,
      decoration: BoxDecoration(
        color: _getUserColor(userName),
        borderRadius: BorderRadius.circular(16),
      ),
      child: Center(
        child: Text(
          userName.isNotEmpty ? userName[0].toUpperCase() : '?',
